package mrp.web.vo;

public class PointProduct {
	private String p_id; // id값
	private String p_division;// 분류
	private String p_name;// 이름
	private int p_point;// 가격
	private int p_count;// 수량
	private String p_image;
	private String p_content;
	private String p_term;

	public String getP_id() {
		return p_id;
	}

	public void setP_id(String p_id) {
		this.p_id = p_id;
	}

	public String getP_division() {
		return p_division;
	}

	public void setP_division(String p_division) {
		this.p_division = p_division;
	}

	public String getP_name() {
		return p_name;
	}

	public void setP_name(String p_name) {
		this.p_name = p_name;
	}

	public int getP_point() {
		return p_point;
	}

	public void setP_point(int p_point) {
		this.p_point = p_point;
	}

	public int getP_count() {
		return p_count;
	}

	public void setP_count(int p_count) {
		this.p_count = p_count;
	}

	public String getP_image() {
		return p_image;
	}

	public void setP_image(String p_image) {
		this.p_image = p_image;
	}

	public String getP_content() {
		return p_content;
	}

	public void setP_content(String p_content) {
		this.p_content = p_content;
	}

	public String getP_term() {
		return p_term;
	}

	public void setP_term(String p_term) {
		this.p_term = p_term;
	}

}
